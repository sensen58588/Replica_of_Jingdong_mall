package com.hxq.design.beans;

public class DealInfo {
		private int dealDataYear;
		private int dealDataMonth;
		private int dealDataDay;
		private int dealNumber;
		private float dealMoney;
		public int getDealDataYear() {
			return dealDataYear;
		}
		public void setDealDataYear(int dealDataYear) {
			this.dealDataYear = dealDataYear;
		}
		public int getDealDataMonth() {
			return dealDataMonth;
		}
		public void setDealDataMonth(int dealDataMonth) {
			this.dealDataMonth = dealDataMonth;
		}
		public int getDealDataDay() {
			return dealDataDay;
		}
		public void setDealDataDay(int dealDataDay) {
			this.dealDataDay = dealDataDay;
		}
		public int getDealNumber() {
			return dealNumber;
		}
		public void setDealNumber(int dealNumber) {
			this.dealNumber = dealNumber;
		}
		public float getDealMoney() {
			return dealMoney;
		}
		public void setDealMoney(float dealMoney) {
			this.dealMoney = dealMoney;
		}

}
